// человеческим годам, после - каждый год равен 4 человеческим годам. Результат распечатай в консоль.
fun petAge (age: Int) {
    if (age < 1) {
        println("0 years")
    }
    else if (age <= 2) {
        println("${age * 10.5} years")
    }
    else {
        println("${age * 4} years")
