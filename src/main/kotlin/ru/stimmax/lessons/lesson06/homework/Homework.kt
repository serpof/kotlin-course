//Задание 6: "Конвертация температуры"
//Создайте функцию, которая конвертирует температуру из градусов Цельсия в Фаренгейты и наоборот в зависимости
// от указанной единицы измерения (C/F). Единицу измерения нужно передать вторым аргументом функции.
// Несколько аргументов передаются через запятую. Распечатай в консоль результат конвертации с добавлением единицы
// измерения. Чтобы добавить единицу измерения после результата, используй функцию печати без переноса строки print("C")
// или print("F").
fun convertTempValue (tempValue: Double, tempUnit: Char) {
    if (tempUnit == 'C') {
        println("${tempValue * 9 / 5 + 32} °F")
    }
    else if (tempUnit == 'F') {
        println("${(tempValue - 32) * 5 / 9} °C")
    }
    else {
        println("Unknown temp unit")
    }
}

